package me.kenzierocks.ourtube.netty;

import io.netty.channel.ChannelInitializer;
import io.netty.channel.ChannelPipeline;
import io.netty.channel.socket.SocketChannel;
import io.netty.handler.codec.http.HttpObjectAggregator;
import io.netty.handler.codec.http.HttpServerCodec;
import io.netty.handler.codec.http.websocketx.WebSocketServerProtocolHandler;
import io.netty.handler.codec.http.websocketx.extensions.compression.WebSocketServerCompressionHandler;
import me.kenzierocks.ourtube.rpc.RpcHelper;

public class WebSocketServerInitializer extends ChannelInitializer<SocketChannel> {

    private static final String WEBSOCKET_PATH = "/gateway";

    private final RpcClientRegistry clients = new RpcClientRegistry();
    private final RpcHelper apiHelper;

    public WebSocketServerInitializer(RpcHelper apiHelper) {
        this.apiHelper = apiHelper;
    }

    @Override
    public void initChannel(SocketChannel ch) throws Exception {
        ChannelPipeline pipeline = ch.pipeline();
        pipeline.addLast(new HttpServerCodec());
        pipeline.addLast(new HttpObjectAggregator(65536));
        pipeline.addLast(new WebSocketServerCompressionHandler());
        pipeline.addLast(new WebSocketServerProtocolHandler(WEBSOCKET_PATH, null, true, 65536, false, true));
        pipeline.addLast(new SimpleHttpServer());
        pipeline.addLast(new WebSocketAuthenticationHandler(clients, apiHelper));
        pipeline.addLast(new WebSocketFrameHandler(clients, apiHelper));
    }
}