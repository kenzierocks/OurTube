package me.kenzierocks.ourtube;

import com.fasterxml.jackson.databind.ObjectMapper;
import com.fasterxml.jackson.datatype.guava.GuavaModule;
import com.fasterxml.jackson.datatype.jdk8.Jdk8Module;

import io.netty.bootstrap.ServerBootstrap;
import io.netty.channel.Channel;
import io.netty.channel.ChannelOption;
import io.netty.channel.EventLoopGroup;
import io.netty.channel.nio.NioEventLoopGroup;
import io.netty.channel.socket.nio.NioServerSocketChannel;
import io.netty.handler.logging.LogLevel;
import io.netty.handler.logging.LoggingHandler;
import me.kenzierocks.ourtube.netty.WebSocketServerInitializer;
import me.kenzierocks.ourtube.rpc.RpcHelper;
import me.kenzierocks.ourtube.rpc.RpcRegistry;

public class WebsocketTask implements Runnable {

    @Override
    public void run() {
        RpcRegistry registry = new RpcRegistry();
        new OurTubeApi(registry).configure();
        RpcHelper helper = new RpcHelper(registry, new ObjectMapper()
                .registerModules(new Jdk8Module(), new GuavaModule()));

        EventLoopGroup bossGroup = new NioEventLoopGroup(1);
        EventLoopGroup workerGroup = new NioEventLoopGroup();
        try {
            ServerBootstrap b = new ServerBootstrap();
            b.group(bossGroup, workerGroup)
                    .channel(NioServerSocketChannel.class)
                    .option(ChannelOption.SO_REUSEADDR, true)
                    .handler(new LoggingHandler(LogLevel.DEBUG))
                    .childHandler(new WebSocketServerInitializer(helper));

            Channel ch = b.bind(13445).sync().channel();

            ch.closeFuture().sync();
        } catch (InterruptedException e) {
            Thread.currentThread().interrupt();
        } finally {
            bossGroup.shutdownGracefully();
            workerGroup.shutdownGracefully();
        }
    }

}
